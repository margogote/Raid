package Models;

public class MalusBonus {

	private String nomMalusBonus;
	private int idMalusBonus;
	private String typeMB;
	private String tempsMB;
	private boolean malus;
	
	public String getNomMalusBonus() {
		return nomMalusBonus;
	}
	public void setNomMalusBonus(String nomMalusBonus) {
		this.nomMalusBonus = nomMalusBonus;
	}
	public int getIdMalusBonus() {
		return idMalusBonus;
	}
	public void setIdMalusBonus(int idMalusBonus) {
		this.idMalusBonus = idMalusBonus;
	}
	public String getTypeMB() {
		return typeMB;
	}
	public void setTypeMB(String typeMB) {
		this.typeMB = typeMB;
	}
	public String getTempsMB() {
		return tempsMB;
	}
	public void setTempsMB(String tempsMB) {
		this.tempsMB = tempsMB;
	}
	public boolean isMalus() {
		return malus;
	}
	public void setMalus(boolean malus) {
		this.malus = malus;
	}
	
	
}
